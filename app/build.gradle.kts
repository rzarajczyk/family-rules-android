import java.io.ByteArrayOutputStream

fun checkGitTag(): String {
    val stdout = ByteArrayOutputStream()
    exec {
        workingDir = rootDir
        commandLine("git", "describe", "--tags", "--abbrev=0")
        standardOutput = stdout
    }
    return stdout.toString().trim()
}

